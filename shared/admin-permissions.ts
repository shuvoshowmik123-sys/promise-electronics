export function getDefaultPermissionsForRole(role: string): Record<string, boolean> {
  switch (role) {
    case "Super Admin":
      return {
        dashboard: true,
        jobs: true,
        inventory: true,
        pos: true,
        challans: true,
        finance: true,
        attendance: true,
        reports: true,
        serviceRequests: true,
        orders: true,
        technician: true,
        inquiries: true,
        systemHealth: true,
        users: true,
        settings: true,
        corporate: true,
        canCreate: true,
        canEdit: true,
        canDelete: true,
        canExport: true,
        process_payment: true,
        view_financials: true,
      };
    case "Manager":
      return {
        dashboard: true,
        jobs: true,
        inventory: true,
        pos: true,
        challans: true,
        finance: true,
        attendance: true,
        reports: true,
        serviceRequests: true,
        orders: true,
        technician: false,
        inquiries: true,
        systemHealth: false,
        users: false,
        settings: false,
        corporate: true,
        canCreate: true,
        canEdit: true,
        canDelete: false,
        canExport: true,
        process_payment: true,
        view_financials: true,
      };
    case "Cashier":
      return {
        dashboard: true,
        jobs: false,
        inventory: true,
        pos: true,
        challans: false,
        finance: false,
        attendance: true,
        reports: false,
        serviceRequests: false,
        orders: true,
        technician: false,
        inquiries: false,
        systemHealth: false,
        users: false,
        settings: false,
        canCreate: true,
        canEdit: false,
        canDelete: false,
        canExport: false,
        process_payment: true,
        view_financials: true,
      };
    case "Technician":
      return {
        dashboard: false,
        jobs: true,
        inventory: false,
        pos: false,
        challans: true,
        finance: false,
        attendance: true,
        reports: false,
        serviceRequests: true,
        orders: false,
        technician: true,
        inquiries: false,
        systemHealth: false,
        users: false,
        settings: false,
        canCreate: false,
        canEdit: true,
        canDelete: false,
        canExport: false,
      };
    case "Corporate":
      return {
        dashboard: true,
        jobs: true,
        finance: true,
        serviceRequests: true,
        reports: true,
        users: false,
        settings: true,
        inventory: false,
        pos: false,
        challans: true,
        attendance: false,
        orders: false,
        technician: false,
        inquiries: true,
        systemHealth: false,
        canCreate: true,
        canEdit: true,
        canDelete: false,
        canExport: true,
        process_payment: false,
        view_financials: true,
      };
    default:
      return {};
  }
}
